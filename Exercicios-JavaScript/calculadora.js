// Treinando funções

// Somar
function somar(valor1=0, valor2=0) {
    return valor1 + valor2
}

console.log('O resultado da função somar(5, 3) é = ', somar(5, 3))


// Subtrair
function menos(valor1=0, valor2=0) {
    return valor1 - valor2
}

console.log('O resultado da função meno(5, 3) é = ', menos(5, 3))

